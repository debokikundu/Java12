package dec04;

public class Array3 {

    public static void main(String[] args) {

        String[] fruits= {"apple", "banana", "strawberry", "orange"}; //length is 4, index is 0,1,2,3

        System.out.println(fruits.length);

        for(int i = 0; i <= fruits.length -1; i++){
            System.out.println(fruits[i]);
        }

    }
}
