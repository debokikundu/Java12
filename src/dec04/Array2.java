package dec04;

public class Array2 {

    public static void main(String[] args) {
        int[] temps = {35, 38, 40, 38, 20, 35, 34, 56, 67, 43, 78, 79, 56, 76};


        System.out.println(temps[0]);
        System.out.println(temps[1]);
        System.out.println(temps[2]);
        System.out.println(temps[3]);
        System.out.println(temps[4]);
        System.out.println(temps[5]);

        for(int i =0; i <= temps.length -1; i++){
            System.out.println(temps[i]);
        }



    }
}
