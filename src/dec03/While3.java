package dec03;

public class While3 {
    public static void main(String[] args) {
        // print 1 - 100
        int a = 1;
        while(a<=100){
            System.out.println(a);
            a++;
        }

        //100 - 1
        int b = 100;
        while( b >= 1){
            System.out.println(b);
            b--;
        }
    }
}
