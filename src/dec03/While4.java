package dec03;

public class While4 {

    public static void main(String[] args) {
        //1 -> 50

        int a = 1; //starting point

        while(a <= 50){ //comparing with ending point
            System.out.println(a);
            a++; //increment/decrement
        }



        for(int b = 1; b<= 50; b++){
            System.out.println(b);
        }
    }
}
